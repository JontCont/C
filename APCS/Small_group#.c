#include <stdio.h>
#include <stdlib.h>

int main()
{
    int count = 0;
    int number, j, k;

    scanf("%d", &number);
    int num_x[1000] = {0};
    int num_y[1000] = {0};

    for (int i = 0; i < number; i++)
        scanf("%d", &num_x[i]);

    for (k = 0; k < number; k++)
    {
        if (num_y[k] == 0)
        {
            count++;
            num_y[k] = 1;
            j = num_x[k];
            while (j != k)
            {
                num_y[j] = 1;
                j = num_x[j];
            }
        }
    }
    printf("%-2d", count);

    return 0;
}